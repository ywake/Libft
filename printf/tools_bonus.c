#include "ft_printf.h"
#include "libft.h"

int		max(int a, int b)
{
	if (a > b)
		return (a);
	return (b);
}

int		abs_p(int *p)
{
	if (*p < 0)
	{
		*p *= -1;
		return (1);
	}
	return (0);
}
